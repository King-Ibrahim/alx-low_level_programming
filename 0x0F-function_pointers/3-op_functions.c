#include <stdio.h>
#include <stdlib.h>
#include "3-calc.h"

/**
 * op_add - The add function
 * @a: input one
 * @b: input two
 * Return: returns 0.
 *
 */

int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - The subtract function
 * @a: input one
 * @b: input two
 * Return: returns 0.
 *
 */

int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - The multiplication function
 * @a: input one
 * @b: input two
 * Return: returns 0.
 *
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - The divide function
 * @a: input one
 * @b: input two
 * Return: returns 0.
 *
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - The modulous function
 * @a: input one
 * @b: input two
 * Return: returns 0.
 *
 */

int op_mod(int a, int b)
{
	return (a % b);
}
